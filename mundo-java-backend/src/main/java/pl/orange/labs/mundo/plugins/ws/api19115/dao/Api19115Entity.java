package pl.orange.labs.mundo.plugins.ws.api19115.dao;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * Represents entity in DB
 * @author Henryk Rosa (henryk.rosa@orange.com)
 *
 */
@Entity
@Table(name="api19115")
public class Api19115Entity 
{
    @Id
    @Column(name = "ID")
    @GeneratedValue(strategy=GenerationType.IDENTITY)    
    private Long id;
    @Column(name="name", unique=true, nullable=false)
    private String name;        
    private String description;
    @Column(name="url", nullable=false)
    private String url;
//    @Column(name="respokcode")
//    private int respOkCode;
    
//TODO - possible additional params for validation of request and response

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    
}
